#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (1)
	{
		if (s[i] == (char)c)
			return ((char*)&s[i]);
		if (!(s[i]))
			return (NULL);
		i++;
	}
}
