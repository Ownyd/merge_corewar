#include "../inc/asm.h"

void	delspaces_tab(char **tab)
{
	char	*tmp;
	int		i;

	i = 0;
	while (tab[i])
	{
		tmp = ft_strtrim(tab[i]);
		free(tab[i]);
		tab[i] = tmp;
		i++;
	}
}
